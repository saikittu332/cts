var btn = document.querySelector(".calculate");
var fromCurrency = document.getElementById("from");
var toCurrency = document.getElementById("to");
var inputAmount = document.getElementById("amount");
var given = document.querySelector(".given-amount");
var toShowBase = document.querySelector(".base-currency");
var toShowSecond = document.querySelector(".second-currency");
var output = document.querySelector(".final-result");

function convertCurrency(event) {
  event.preventDefault();
  var amount = inputAmount.value;
  var from = fromCurrency.value;
  var to = toCurrency.value;
  var result = 0;

  try {
    if (from == to) {
      result = amount;
    } else {
      result = amount * currency[from][to];
    }
  } catch (err) {
    result = amount * (1 / currency[to][from]);
  }

  given.innerHTML = amount;
  toShowBase.textContent = from + " = ";
  toShowSecond.textContent = to;
  output.textContent = result;
}

btn.addEventListener("click", convertCurrency);
